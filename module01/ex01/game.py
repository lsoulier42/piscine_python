class GotCharacter:
    def __init__(self, name=None, is_alive=True):
        if type(name) == str and len(name) > 0 and type(is_alive) == bool:
            self.name = name
            self.is_alive = is_alive
        else:
            print("Wrong type of argument for object GotCharacter")
            del self
            exit()

class Lannister(GotCharacter):
    def __init__(self, name=None, is_alive=True):
        super().__init__(name, is_alive)
        self.family_name = "Lannister"
        self.house_words = "Hear Me Roar!"

    def print_house_words(self):
        print(self.house_words)

    def die(self):
        self.is_alive = False

